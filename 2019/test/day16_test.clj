(ns day16-test
  (:require [day16 :as sut]
            [clojure.test :as t]))

(sut/digit-pattern 0)
(take 15 (sut/digit-pattern 1))
(take 15 (sut/digit-pattern 2))
(take 15 (sut/digit-pattern 3))

(sut/part1 1 "12345678")
;; => (4 8 2 2 6 1 5 8)
(sut/part1 2 "12345678")
;; => (3 4 0 4 0 4 3 8)
(sut/part1 3 "12345678")
;; => (0 3 4 1 5 5 1 8)
(sut/part1 4 "12345678")
;; => (0 1 0 2 9 4 9 8)


